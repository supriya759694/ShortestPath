int mindistance(int distance[], bool stat[], int n) {
    int minimum = INT_MAX;
    int index;
    for (int k = 0; k < n; k++) {
        if (!stat[k] && distance[k] <= minimum) {
            minimum = distance[k];
            index = k;
        }
    }
    return index;
}

void dijkstra(int graph[53][53], int source, string stations[], int n) {
    int distance[53];
    bool stat[53] = { false };

    for (int k = 0; k < n; k++) {
        distance[k] = INT_MAX;
    }
    distance[source] = 0;

    for (int k = 0; k < n; k++) {
        int m = mindistance(distance, stat, n);
        stat[m] = true;

        for (int i = 0; i < n; i++) {
            if (!stat[i] && graph[m][i] && distance[m] != INT_MAX && distance[m] + graph[m][i] < distance[i]) {
                distance[i] = distance[m] + graph[m][i];
            }
        }
    }

    cout << "Minimum Number of stations from " << stations[source] << " to every station:" << endl;
    for (int k = 0; k < n; k++) {
        if (distance[k] == INT_MAX) {
            cout << "No path from " << stations[source] << " to " << stations[k] << endl;
        } else {
            cout << "Minimum stations from " << stations[source] << " to " << stations[k] << " are " << distance[k] << endl;
        }
    }
}

